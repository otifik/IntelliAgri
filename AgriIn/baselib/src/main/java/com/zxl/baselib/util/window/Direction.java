package com.zxl.baselib.util.window;

/**
 * 定义了从左到右，从上到下，从右到左，从下到上四个方向的类
 *
 * @author zxl
 * @date 2017/2/7
 */

public enum Direction {
    /**
     * 从左至右
     */
    LEFT_TO_RIGHT,
    /**
     * 从上至下
     */
    TOP_TO_BOTTOM,
    /**
     * 从右至左
     */
    RIGHT_TO_LEFT,
    /**
     * 从下至上
     */
    BOTTOM_TO_TOP
}

